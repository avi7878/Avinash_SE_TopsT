# How will you remove last object from a list?
# Suppose list1 is [2, 33, 222, 14, and 25], what is list1 [-1]? 

# ==> use pop() to Remove the last object from the list.

l1 = ['python','java','android','php'] 
l1.pop() # remove the last object.
print(l1)

l2 = [2, 33, 222, 14,25]
print(l2[-1])

# in l2 [-1] is 25.